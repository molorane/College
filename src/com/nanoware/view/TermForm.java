    private void btnAddTermActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAddTermActionPerformed
        // TODO add your handling code here:
        if(txtTermYear.getText().length()>0 && txtCategory1.getText().length()>0 && txtCategory2.getText().length()>0){
            String termYear = txtTermYear.getText();
            String category1 = (txtCategory1.getText().length() == 2)? txtCategory1.getText(): 
                                            (txtCategory1.getText().length() < 2)? "0"+txtCategory1.getText():"00";
            String dept = jcbAddDepartment.getSelectedItem().toString();
            String category2 = (txtCategory2.getText().length() == 2)? txtCategory2.getText(): 
                                            (txtCategory2.getText().length() < 2)? "0"+txtCategory2.getText():"00";

            try{
                int yr = Integer.parseInt(termYear);
                int yr1 = Integer.parseInt(txtCategory1.getText());
                int yr2 = Integer.parseInt(txtCategory2.getText());
                
                String term = termYear+"-"+category1+"-"+dept+"-"+category2;
                Term t = new Term();
                t.setTermId(-1);
                t.setTerm(term);
                int added = termImpl.AddTerm(t);
                if(added > 0){
                    display();
                    Functions.successMessage("Term saved..");
                }else{
                    Functions.successMessage("Term not saved..");
                }
            }catch(NumberFormatException ex){
                Functions.errorMessage(ex.getLocalizedMessage());
            }
        }else{
            Functions.warningMessage("Fill values for all fields.");
        }
    }//GEN-LAST:event_btnAddTermActionPerformed

