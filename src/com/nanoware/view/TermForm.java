    private void fillTermsByYear(int year){        
        ArrayList<Term> terms = termImpl.GetTermsByYear(year);
        displayTerms(terms);   
    }
    
    private void fillTermsByDepartment(String department){        
        ArrayList<Term> terms = termImpl.GetTermsByDepartment(department);
        displayTerms(terms);   
    }
    
    private void fillTermsByYearAndDepartment(int year,String department){        
        ArrayList<Term> terms = termImpl.GetTermsByYearAndDepartment(year, department);
        displayTerms(terms);   
    }
    
    private void displayTerms(ArrayList<Term> terms){
        tblModelTerms.setRowCount(0);
        terms.forEach((term) -> {
            tblModelTerms.addRow(new Object[]{
               term.getTermId(), 
               term.getTerm()
            });
        });  
    }
    
    public void display(){
        int year = Integer.parseInt(jcbTermYear.getSelectedItem().toString());
        String department = jcbDepartment.getSelectedItem().toString();
        String dept = Functions.getDeptFromDepartment(department);
        fillTermsByYearAndDepartment(year,dept);
    }

